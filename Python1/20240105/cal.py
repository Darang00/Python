def plus(x, y):
    result = x + y

    return result

def minus(x, y):
    result = x - y

    return result

def times(x, y):
    result = x * y

    return result

def devide(x, y):
    result = x / y

    return round(result, 2)

def rst(x, y): #몫
    result = x // y

    return result

def rest(x, y): #나머지
    result = x % y

    return result


    
    
    
